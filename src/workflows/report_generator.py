"""
Report Generator workflow (supports multi-turn conversation and visual enhancement)

Features:
- Input: User provides a set of search keywords
- Step 1: Call ResearcherAgent to search top N projects
- Step 2: Iterate through projects, call AnalystAgent for in-depth analysis
- Step 3: Aggregate all analysis results into a Markdown-formatted report
- Step 4 (new): Support user follow-up questions for multi-turn conversation
- Visual enhancement: Generate text progress bars for Star counts

This is a linear workflow demonstrating the basic pattern of multi-Agent collaboration.
"""

