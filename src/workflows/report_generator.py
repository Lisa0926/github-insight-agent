    @trace(name="report.handle_followup")
    def handle_followup(
        self,
        user_query: str,
    ) -> str:
        """
        Handle user follow-up questions (multi-turn conversation support)

        Args:
            user_query: User follow-up question

        Returns:
            Assistant response
        """
        logger.info(f"Handling followup: {user_query}")

